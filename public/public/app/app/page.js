"use client";

import { useState } from "react";

export default function Home() {
  const [orgName, setOrgName] = useState("");
  const [projectName, setProjectName] = useState("");
  const [duration, setDuration] = useState("");
  const [summary, setSummary] = useState("");
  const [output, setOutput] = useState("");

  const generateReport = () => {
    const report = `
CSO REPORT
------------------------
Organisation Name: ${orgName}
Project Name: ${projectName}
Duration: ${duration}

Project Summary:
${summary}

Generated By: CSO Report Create App
Date: ${new Date().toLocaleDateString()}
    `;

    setOutput(report);
  };

  return (
    <div style={{ padding: "30px", maxWidth: "900px", margin: "auto" }}>
      <h1 style={{ textAlign: "center", color: "#0f172a" }}>
        CSO Report Create App
      </h1>

      <p style={{ textAlign: "center", color: "#475569" }}>
        Auto generate CSO reports quickly.
      </p>

      <div style={{ marginTop: "30px", padding: "20px", border: "1px solid #ddd", borderRadius: "10px" }}>
        <label>Organisation Name</label>
        <input
          type="text"
          value={orgName}
          onChange={(e) => setOrgName(e.target.value)}
          style={{ width: "100%", padding: "10px", marginTop: "5px", marginBottom: "15px" }}
        />

        <label>Project Name</label>
        <input
          type="text"
          value={projectName}
          onChange={(e) => setProjectName(e.target.value)}
          style={{ width: "100%", padding: "10px", marginTop: "5px", marginBottom: "15px" }}
        />

        <label>Project Duration</label>
        <input
          type="text"
          value={duration}
          onChange={(e) => setDuration(e.target.value)}
          placeholder="e.g. Jan 2025 - Dec 2025"
          style={{ width: "100%", padding: "10px", marginTop: "5px", marginBottom: "15px" }}
        />

        <label>Project Summary</label>
        <textarea
          value={summary}
          onChange={(e) => setSummary(e.target.value)}
          rows={6}
          style={{ width: "100%", padding: "10px", marginTop: "5px", marginBottom: "15px" }}
        />

        <button
          onClick={generateReport}
          style={{
            width: "100%",
            padding: "12px",
            background: "#2563eb",
            color: "white",
            border: "none",
            borderRadius: "8px",
            cursor: "pointer",
            fontSize: "16px"
          }}
        >
          Generate Report
        </button>
      </div>

      {output && (
        <div style={{ marginTop: "30px", padding: "20px", border: "1px solid #ddd", borderRadius: "10px", background: "#f8fafc" }}>
          <h2>Generated Report</h2>
          <pre style={{ whiteSpace: "pre-wrap", fontSize: "14px" }}>{output}</pre>
        </div>
      )}
    </div>
  );
}
